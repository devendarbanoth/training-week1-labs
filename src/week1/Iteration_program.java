package week1;

public class Iteration_program {
    public static void main(String[] args) {

        int a = 10;
        while(a>0){
            System.out.println("The value of a = " + a);
            a--;
        }

        System.out.println("\n\n");

        int b=10;
        do{
            System.out.println("The value of b = " + b);
            b--;
        }while(b>0);
    }
}
