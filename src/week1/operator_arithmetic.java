package week1;

public class operator_arithmetic {
    public static void main(String[] args) {

        Integer a,b,c;

        a = 4;
        b = 23;

        System.out.println(b+a);
        System.out.println(b-a);
        System.out.println(b*a);
        System.out.println(b/a); // quotient
        System.out.println(b%a); // reminder
        System.out.println(a);
        System.out.println(++a); // increments a by 1 and then prints
        System.out.println(a++); // First prints a and then increments it
        System.out.println(a);
        System.out.println(b++); // First prints b and then increments it

    }
}
